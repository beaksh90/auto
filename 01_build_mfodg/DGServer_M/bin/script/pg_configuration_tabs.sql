CREATE TABLE APM_DB_INFO
(
  DB_ID            SMALLINT               ,
  INSTANCE_NAME    CHARACTER VARYING(64)  ,
  HOST_IP          CHARACTER VARYING(16)  ,
  HOST_NAME        CHARACTER VARYING(64)  ,
  PORT             INTEGER                ,
  DAEMON_USER      CHARACTER VARYING(64)  ,
  DAEMON_PASSWORD  CHARACTER VARYING(64)  ,
  DB_USER          CHARACTER VARYING(64)  ,
  DB_PASSWORD      CHARACTER VARYING(64)  ,
  JDBC_URL         CHARACTER VARYING(512) ,
  CHAR_SET         CHARACTER VARYING(64)  ,
  ALERT_GROUP_NAME CHARACTER VARYING(64)  ,
  RAC_GROUP_ID     INTEGER                ,
  GATHER_ID        INTEGER                ,
  DB_TYPE          CHARACTER VARYING(16)  ,
  SID              CHARACTER VARYING(64)  ,
  LSNR_PORT        INTEGER                ,
  OS_TYPE          CHARACTER VARYING(64)  ,
  ORACLE_VERSION   CHARACTER VARYING(64)
);

CREATE UNIQUE INDEX APM_DB_INFO_UX ON APM_DB_INFO ( DB_ID );
CREATE INDEX APM_DB_INFO_IX ON APM_DB_INFO ( DB_ID, INSTANCE_NAME );


CREATE TABLE ORA_RAC_GROUP_NAME
(
  RAC_GROUP_ID INTEGER                ,
  NAME         CHARACTER VARYING(16)  ,
  DESCRIPTION  CHARACTER VARYING(512)
);

CREATE UNIQUE INDEX ORA_RAC_GROUP_NAME_UX ON ORA_RAC_GROUP_NAME ( RAC_GROUP_ID );


CREATE TABLE ORA_SERVICE_INFO 
(
  SERVICE_ID INTEGER  ,
  DB_ID      SMALLINT
);

CREATE UNIQUE INDEX ORA_SERVICE_INFO_UX ON ORA_SERVICE_INFO ( DB_ID );


CREATE TABLE ORA_SERVICE_NAME 
(
  SERVICE_ID  INTEGER                ,
  NAME        CHARACTER VARYING(64)  ,
  DESCRIPTION CHARACTER VARYING(512)
);

CREATE UNIQUE INDEX ORA_SERVICE_NAME_UX ON ORA_SERVICE_NAME ( SERVICE_ID );


CREATE TABLE ORA_LC_CONFIG 
(
  DB_ID                     SMALLINT               ,
  DOWNLOAD_PARAMETER        CHARACTER VARYING(1)   ,
  DOWNLOAD_TABLESPACE       CHARACTER VARYING(1)   ,
  DOWNLOAD_SGASTAT          CHARACTER VARYING(1)   ,
  DOWNLOAD_SGASTAT_INTERVAL BIGINT                 ,
  DOWNLOAD_SGASTAT_SQL      CHARACTER VARYING(512) ,
  PLANNER_RUN               CHARACTER VARYING(1)   ,
  PLANNER_SCHEMAS           CHARACTER VARYING(512) ,
  PLANNER_MAKE_BIND         CHARACTER VARYING(1)   ,
  PLANNER_USE_EXPLAIN       CHARACTER VARYING(1)   ,
  LAST_MODIFIED_TIME        TIMESTAMP 
);

CREATE UNIQUE INDEX ORA_LC_CONFIG_UX ON ORA_LC_CONFIG ( DB_ID );
